    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        //"+-=*&| {}()[]!?^/%;:.,<>\n\t\r\b\f"
        ArrayList<Integer> palabras = new ArrayList<>();

        Lexico analisisLexico = new Lexico(getTexto(),"()}{,-;:<>=. \t\n\r3", url);
        Automata s = new Automata(getTexto(), analisisLexico.getVecAlfabeto(), analisisLexico.getMatrizTransaccionGeneral());
        s.desCadena();

        s.desVectorAlfabeto();
        s.desMatrizTrasicion();
        ArrayList<Lexema> lexemas = analisisLexico.analisisLexico();
      
        
        String textoMostrar = "";
        for (int i = 0; i < lexemas.size(); i++) {
            textoMostrar += " " + lexemas.get(i).getLexema() + "\t" + lexemas.get(i).getNombreToken() + "\t" + lexemas.get(i).getNumToken() + "\t" + lexemas.get(i).getRenglon() + "\n";
            palabras.add(lexemas.get(i).getNumToken());
        }

        ePP.getTxtPanelCompilando().setText(textoMostrar);

        ManipulaArchivos.guardar(String.valueOf(palabras), ePP.rutaArchivo.substring(0, ePP.rutaArchivo.length() - 4) + "_analisislexico.txt");
        //char[] separador = txtSeparadores.getText().toCharArray();
        //palabras=clases.Separador.separarCodigo(ePP.getTxtPanelCompilando(),getTexto(),txtSeparadores.getText()); 
    
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

